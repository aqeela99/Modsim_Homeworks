import random
Q = []
for t in range(10):
  num = random.randint(1,10)
  Q.append(num) #The append() method in python adds a single item to the existing list. 
                #It doesnâ€™t return a new list of items but will modify the original list 
                #by adding the item to the end of the list.

simPeriod = 100
serverBusy = False #boolean false
currentService=0
qSize = [] #empty list
for t in range(simPeriod):
  print('At time: ', t)
  if not serverBusy and len(Q)>0: # len returns the length of string or data
          currentService=Q.pop() #number between the range of 1-10 will be popped out
          print(" Next customer is being served,with service time=", currentService)
          serverBusy = True
  elif serverBusy and len(Q)>=0:
        currentService=currentService-1
        print('Server currently busy. Time left: ', currentService, ' minutes')
        if currentService == 0:
          serverBusy = False
  elif len(Q) <= 0:
        print('Server is not Busy.')
  qSize.append(len(Q))